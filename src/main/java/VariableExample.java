public class VariableExample {
    public static void main(String[] args) {
        // Declaring and initializing variables with suitable data types
        int age = 30;               // Suitable data type for age (integer value)
        double temperature = 36.5;   // Suitable data type for temperature (decimal value)
        char grade = 'A';           // Suitable data type for grade (single character)

        // Printing the values of the variables
        System.out.print("Age: " + age + ", ");
        System.out.print("Temperature: " + temperature + ", ");
        System.out.print("Grade: " + grade);
    }
}